void Parser::Statement(StatementRecord*&ptrToStmtRecord) {
		ExprRecord expr; 
		if (StartOf(10)) {
			switch (la->kind) {
			case _ident: case _string: case _integer: case _real: case _character: case 15 /* "+" */: case 16 /* "-" */: case 35 /* "(" */: case 43 /* "NIL" */: case 44 /* "~" */: case 48 /* "{" */: {
				Expr(expr);
				if (la->kind == 23 /* ":=" */) {
					Get();
					Expr(expr);
				}
				break;
			}
			case 50 /* "IF" */: {
				Get();
				Expr(expr);
				Expect(51 /* "THEN" */);
				StatementSeq(thenStmtSeq);
				while (la->kind == 52 /* "ELSIF" */) {
					Get();
					Expr(expr);
					Expect(51 /* "THEN" */);
					StatementSeq(elsifThenStatementSeq);
				}
				if (la->kind == 53 /* "ELSE" */) {
					Get();
					StatementSeq(statementSeqELSE);
				}
				Expect(33 /* "END" */);
				break;
			}
			case 54 /* "CASE" */: {
				Get();
				Expr(expr);
				Expect(38 /* "OF" */);
				Case();
				while (la->kind == 55 /* "|" */) {
					Get();
					Case();
				}
				if (la->kind == 53 /* "ELSE" */) {
					Get();
					StatementSeq(statementSeqELSE);
				}
				Expect(33 /* "END" */);
				break;
			}
			case 56 /* "WHILE" */: {
				Get();
				Expr(expr);
				Expect(57 /* "DO" */);
				StatementSeq(whileBodyStatementSeq);
				Expect(33 /* "END" */);
				break;
			}
			case 58 /* "REPEAT" */: {
				Get();
				StatementSeq(repeatBodyStatementSeq);
				Expect(59 /* "UNTIL" */);
				Expr(expr);
				break;
			}
			case 60 /* "FOR" */: {
				Get();
				Ident(forCounterVariableName);
				Expect(23 /* ":=" */);
				Expr(expr);
				Expect(41 /* "TO" */);
				Expr(expr);
				if (la->kind == 61 /* "BY" */) {
					Get();
					ConstExpr(expr);
				}
				Expect(57 /* "DO" */);
				StatementSeq(forStatementSeq);
				Expect(33 /* "END" */);
				break;
			}
			case 62 /* "LOOP" */: {
				Get();
				StatementSeq(loopStatementSeq);
				Expect(33 /* "END" */);
				break;
			}
			case 63 /* "WITH" */: {
				Get();
				Guard();
				Expect(57 /* "DO" */);
				StatementSeq(statementSeq1);
				while (la->kind == 55 /* "|" */) {
					Get();
					Guard();
					Expect(57 /* "DO" */);
					StatementSeq(statementSeqN);
				}
				if (la->kind == 53 /* "ELSE" */) {
					Get();
					StatementSeq(statementSeqELSE);
				}
				Expect(33 /* "END" */);
				break;
			}
			case 64 /* "EXIT" */: {
				Get();
				break;
			}
			case 65 /* "RETURN" */: {
				Get();
				if (StartOf(8)) {
					Expr(expr);
				}
				break;
			}
			}
		}
}

