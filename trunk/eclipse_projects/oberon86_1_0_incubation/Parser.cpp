void Parser::Relation(int &op) {
		op = illegal_operator; 
		switch (la->kind) {
		case 7 /* "=" */: {
			Get();
			op = equals; 
			break;
		}
		case 8 /* "#" */: {
			Get();
			op = notEquals; 
			break;
		}
		case 9 /* "<" */: {
			Get();
			op = less; 
			break;
		}
		case 10 /* "<=" */: {
			Get();
			op = lessOrEqual; 
			break;
		}
		case 11 /* ">" */: {
			Get();
			op = greater; 
			break;
		}
		case 12 /* ">=" */: {
			Get();
			op = greaterOrEqual; 
			break;
		}
		case 13 /* "IN" */: {
			Get();
			op = in; 
			break;
		}
		case 14 /* "IS" */: {
			Get();
			op = is; 
			break;
		}
		default: SynErr(69); break;
		}
}

